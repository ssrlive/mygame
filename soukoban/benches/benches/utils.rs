use soukoban::Level;
use std::{fs, path::Path};

pub fn load_level_from_file<P: AsRef<Path>>(path: P, id: usize) -> Level {
    assert!(id >= 1);
    Level::load_nth_from_str(&fs::read_to_string(path).unwrap(), id).unwrap()
}

